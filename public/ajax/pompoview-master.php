<h2 class="pretty">PompOView</h2>
<p>
	<strong>Introduction :</strong> PompOView est un projet universitaire qui a pour but de faire un frontend pour la sortie de l'outils <a href="http://code.google.com/p/pypometre/" onclick="window.open(this.href); return false;"><strong>Pomp-o-metre</strong></a> et ainsi de permettre a la fois la détéction de plagia au sein d'un corpus de documents, soit de permettre detecté la redoncdance de code dans un même corpus (dans le cas de grande équipe de travail par exemple).
